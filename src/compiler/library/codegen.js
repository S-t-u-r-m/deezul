/**
 * codegen.js - Code Generator
 *
 * Generates the final compiled component output.
 * Takes all compilation artifacts and produces JavaScript module code.
 *
 * Output format (see docs/compiled-format.md):
 * - template: Static HTML with placeholders
 * - binding: { ref: [...], code: Uint16Array }
 * - eval: Array of expression functions
 * - event: Array of event definitions
 * - dynamic: Array of dynamic blocks
 * - slot: Array of slot definitions
 * - data, method, computed, static, watcher: Script sections
 * - style: Component CSS
 */

import { BIND_TYPE, HANDLER_TYPE } from './detector.js';

/**
 * Generate compiled component code
 * @param {object} compilation - Full compilation result
 * @returns {string} JavaScript module code
 */
export function generateCode(compilation) {
	const {
		templateHTML,
		bytecode,
		bindings,
		evalFunctions,
		dynamics,
		slots,
		refs,
		componentName,
		// Script sections (from full component parsing)
		data,
		method,
		computed,
		staticData,
		watcher,
		style,
		// Lifecycle hooks
		$created,
		$mounted,
		$updated,
		$unmounted,
		$error
	} = compilation;

	const lines = [];

	// Header comment
	lines.push(`/**`);
	lines.push(` * Compiled component: ${componentName || 'Anonymous'}`);
	lines.push(` * Generated by @deezul/compiler`);
	lines.push(` * DO NOT EDIT - This file is auto-generated`);
	lines.push(` */`);
	lines.push('');

	// Single default export
	lines.push(`export default {`);

	// Template
	lines.push(`\ttemplate: ${JSON.stringify(templateHTML)},`);
	lines.push('');

	// Binding: strings table + bytecode
	// strings contains all string references (property paths, attribute names, etc.)
	// bytecode indices reference into strings array
	lines.push(`\tbinding: {`);
	lines.push(`\t\tstrings: ${JSON.stringify(compilation.strings)},`);
	lines.push(`\t\tcode: new Uint16Array([`);
	lines.push(formatBytecodeArray(bytecode));
	lines.push(`\t\t])`);
	lines.push(`\t},`);
	lines.push('');

	// Eval functions
	lines.push(`\teval: ${generateEvalArray(evalFunctions)},`);
	lines.push('');

	// Event definitions
	lines.push(`\tevent: ${generateEventArray(bindings)},`);
	lines.push('');

	// Dynamic blocks
	lines.push(`\tdynamics: ${generateDynamicsArray(dynamics)},`);
	lines.push('');

	// Slots
	if (slots && slots.length > 0) {
		lines.push(`\tslot: ${generateSlotsArray(slots)},`);
		lines.push('');
	}

	// Refs
	if (refs && refs.length > 0) {
		lines.push(`\trefs: ${generateRefsArray(refs)},`);
		lines.push('');
	}

	// Script sections (if present)
	if (data) {
		lines.push(`\tdata: ${data},`);
		lines.push('');
	}

	if (method) {
		lines.push(`\tmethod: ${method},`);
		lines.push('');
	}

	if (computed) {
		lines.push(`\tcomputed: ${computed},`);
		lines.push('');
	}

	if (staticData) {
		lines.push(`\tstatic: ${staticData},`);
		lines.push('');
	}

	if (watcher) {
		lines.push(`\twatch: ${watcher},`);
		lines.push('');
	}

	// Lifecycle hooks
	if ($created) {
		lines.push(`\t$created: ${$created},`);
		lines.push('');
	}
	if ($mounted) {
		lines.push(`\t$mounted: ${$mounted},`);
		lines.push('');
	}
	if ($updated) {
		lines.push(`\t$updated: ${$updated},`);
		lines.push('');
	}
	if ($unmounted) {
		lines.push(`\t$unmounted: ${$unmounted},`);
		lines.push('');
	}
	if ($error) {
		lines.push(`\t$error: ${$error},`);
		lines.push('');
	}

	// Style
	if (style) {
		lines.push(`\tstyle: ${JSON.stringify(style)}`);
	}

	// Remove trailing comma from last property
	const lastLine = lines[lines.length - 1];
	if (lastLine.endsWith(',')) {
		lines[lines.length - 1] = lastLine.slice(0, -1);
	}

	lines.push(`};`);
	lines.push('');

	return lines.join('\n');
}

/**
 * Format bytecode array for readable output
 * Variable-length entries: [type, pathLen, ...path, ...data]
 *
 * EVAL bindings have deps embedded:
 * - TEXT_EVAL: [type, pathLen, ...path, evalIdx, depsLen, ...depIndices]
 * - ATTR_EVAL: [type, pathLen, ...path, attrIdx, evalIdx, depsLen, ...depIndices]
 */
function formatBytecodeArray(bytecode) {
	if (!bytecode || bytecode.length === 0) {
		return '';
	}

	const values = Array.from(bytecode);
	const lines = [];
	let offset = 0;
	let bindingIdx = 0;

	while (offset < values.length) {
		const type = values[offset];
		const pathLen = values[offset + 1];

		// Calculate entry length based on type
		let entryLen;
		const dataOffset = offset + 2 + pathLen;

		switch (type) {
			case BIND_TYPE.TEXT:
			case BIND_TYPE.TWO_WAY:
				// [type, pathLen, ...path, refIdx]
				entryLen = 2 + pathLen + 1;
				break;

			case BIND_TYPE.TEXT_EVAL: {
				// [type, pathLen, ...path, evalIdx, depsLen, ...depIndices]
				const depsLen = values[dataOffset + 1];
				entryLen = 2 + pathLen + 2 + depsLen; // +2 for evalIdx and depsLen
				break;
			}

			case BIND_TYPE.ATTR:
			case BIND_TYPE.EVENT:
			case BIND_TYPE.PROP:
			case BIND_TYPE.PROP_SYNC:
				// [type, pathLen, ...path, nameIdx, valueIdx/handlerType]
				entryLen = 2 + pathLen + 2;
				break;

			case BIND_TYPE.ATTR_EVAL: {
				// [type, pathLen, ...path, attrIdx, evalIdx, depsLen, ...depIndices]
				const depsLen = values[dataOffset + 2];
				entryLen = 2 + pathLen + 3 + depsLen; // +3 for attrIdx, evalIdx, depsLen
				break;
			}

			default:
				entryLen = 2 + pathLen + 1;
		}

		const chunk = values.slice(offset, offset + entryLen);
		lines.push(`\t\t\t${chunk.join(', ')}, /* ${bindingIdx} */`);

		offset += entryLen;
		bindingIdx++;
	}

	return lines.join('\n');
}

/**
 * Generate eval array with deduplication
 * Each eval is just a function - deps are embedded in bytecode
 * Bound to component instance at runtime via .call(this)
 * For :for loops, iterator/index are passed as function parameters
 * so the eval can access loop variables directly (e.g. item.name.charAt(0))
 */
function generateEvalArray(evalFunctions, loopVars) {
	if (!evalFunctions || evalFunctions.length === 0) {
		return '[]';
	}

	const skipSet = loopVars ? new Set(loopVars) : null;
	const params = loopVars ? loopVars.join(', ') : '';

	const lines = evalFunctions.map(({ expression }) => {
		const transformed = transformExpression(expression, skipSet);
		return loopVars
			? `\t\tfunction(${params}) { return ${transformed}; }`
			: `\t\tfunction() { return ${transformed}; }`;
	});

	return `[\n${lines.join(',\n')}\n\t]`;
}

/**
 * Generate event array from bindings
 * Format: ["eventName", "methodName"] or ["eventName", "methodName", ...args]
 */
function generateEventArray(bindings) {
	const events = bindings.filter(b => b.type === BIND_TYPE.EVENT);

	if (events.length === 0) {
		return '[]';
	}

	const lines = events.map(binding => {
		const { eventName, handler } = binding;

		if (handler.type === HANDLER_TYPE.METHOD) {
			// Simple method reference
			return `\t\t[${JSON.stringify(eventName)}, ${JSON.stringify(handler.method)}]`;
		} else if (handler.type === HANDLER_TYPE.CALL) {
			// Method with args
			const args = handler.args ? handler.args.split(',').map(a => a.trim()) : [];
			const parts = [JSON.stringify(eventName), JSON.stringify(handler.method), ...args.map(a => JSON.stringify(a))];
			return `\t\t[${parts.join(', ')}]`;
		} else {
			// Inline expression - store as object with eval
			// Uses function() for this binding, $event available as parameter
			return `\t\t{ event: ${JSON.stringify(eventName)}, eval: function($event) { ${transformExpression(handler.expression)} } }`;
		}
	});

	return `[\n${lines.join(',\n')}\n\t]`;
}

/**
 * Generate dynamics array
 */
function generateDynamicsArray(dynamics) {
	if (!dynamics || dynamics.length === 0) {
		return '[]';
	}

	const items = dynamics.map(d => {
		if (d.type === 'for') {
			return generateForDynamic(d);
		} else if (d.type === 'conditional') {
			return generateConditionalDynamic(d);
		}
		return 'null';
	});

	return `[\n${items.join(',\n')}\n\t]`;
}

/**
 * Generate :for dynamic block
 */
function generateForDynamic(dynamic) {
	const lines = ['\t\t{'];
	lines.push(`\t\t\ttype: 'for',`);
	lines.push(`\t\t\tmarkerIndex: ${dynamic.markerIndex},`);
	lines.push(`\t\t\tmarkerPath: ${JSON.stringify(dynamic.markerPath)},`);
	lines.push(`\t\t\tsource: ${JSON.stringify(dynamic.source)},`);
	lines.push(`\t\t\titerator: ${JSON.stringify(dynamic.iteratorVar)},`);

	if (dynamic.indexVar) {
		lines.push(`\t\t\tindexVar: ${JSON.stringify(dynamic.indexVar)},`);
	}

	// Nested compiled template
	if (dynamic.compiled) {
		lines.push(`\t\t\ttemplate: ${JSON.stringify(dynamic.compiled.templateHTML)},`);
		lines.push(`\t\t\tbinding: ${generateNestedBinding(dynamic.compiled)},`);
		const loopVars = [dynamic.iteratorVar];
		if (dynamic.indexVar) loopVars.push(dynamic.indexVar);
		lines.push(`\t\t\teval: ${generateEvalArray(dynamic.compiled.evalFunctions, loopVars)},`);
		lines.push(`\t\t\tevent: ${generateEventArray(dynamic.compiled.bindings)},`);

		// Recursive dynamics
		if (dynamic.compiled.dynamics && dynamic.compiled.dynamics.length > 0) {
			lines.push(`\t\t\tdynamics: ${generateDynamicsArray(dynamic.compiled.dynamics)}`);
		} else {
			lines.push(`\t\t\tdynamics: []`);
		}
	}

	lines.push('\t\t}');
	return lines.join('\n');
}

/**
 * Generate conditional dynamic block
 */
function generateConditionalDynamic(dynamic) {
	const lines = ['\t\t{'];
	lines.push(`\t\t\ttype: 'if',`);
	lines.push(`\t\t\tmarkerIndex: ${dynamic.markerIndex},`);
	lines.push(`\t\t\tmarkerPath: ${JSON.stringify(dynamic.markerPath)},`);

	// Chain items (if/else-if/else)
	const chainCode = dynamic.chain.map((item) => {
		const itemLines = ['\t\t\t\t{'];

		if (item.condition !== undefined) {
			itemLines.push(`\t\t\t\t\tcondition: ${JSON.stringify(item.condition)},`);
		}

		if (item.compiled) {
			itemLines.push(`\t\t\t\t\ttemplate: ${JSON.stringify(item.compiled.templateHTML)},`);
			itemLines.push(`\t\t\t\t\tbinding: ${generateNestedBinding(item.compiled)},`);
			itemLines.push(`\t\t\t\t\teval: ${generateEvalArray(item.compiled.evalFunctions)},`);
			itemLines.push(`\t\t\t\t\tevent: ${generateEventArray(item.compiled.bindings)},`);
			// Output nested dynamics (recursive)
			if (item.compiled.dynamics && item.compiled.dynamics.length > 0) {
				itemLines.push(`\t\t\t\t\tdynamics: ${generateDynamicsArray(item.compiled.dynamics)}`);
			} else {
				itemLines.push(`\t\t\t\t\tdynamics: []`);
			}
		}

		itemLines.push('\t\t\t\t}');
		return itemLines.join('\n');
	});

	lines.push(`\t\t\tchain: [\n${chainCode.join(',\n')}\n\t\t\t]`);
	lines.push('\t\t}');
	return lines.join('\n');
}

/**
 * Generate nested binding object { strings, code }
 */
function generateNestedBinding(compiled) {
	const stringsTable = JSON.stringify(compiled.strings || []);
	const bytecodeStr = compiled.bytecode && compiled.bytecode.length > 0
		? `new Uint16Array([${Array.from(compiled.bytecode).join(', ')}])`
		: 'new Uint16Array([])';

	return `{ strings: ${stringsTable}, code: ${bytecodeStr} }`;
}

/**
 * Generate slots array
 */
function generateSlotsArray(slots) {
	if (!slots || slots.length === 0) {
		return '[]';
	}

	const items = slots.map(slot => {
		const parts = [
			`name: ${JSON.stringify(slot.name)}`,
			`path: ${JSON.stringify(slot.path)}`
		];
		// Include hasFallback if slot has default content
		if (slot.hasFallback) {
			parts.push('hasFallback: true');
		}
		return `\t\t{ ${parts.join(', ')} }`;
	});

	return `[\n${items.join(',\n')}\n\t]`;
}

/**
 * Generate refs array
 */
function generateRefsArray(refs) {
	if (!refs || refs.length === 0) return '[]';

	const items = refs.map(ref => {
		return `\t\t{ path: ${JSON.stringify(ref.path)}, name: ${JSON.stringify(ref.name)} }`;
	});

	return `[\n${items.join(',\n')}\n\t]`;
}

/**
 * Transform expression to access properties through `this`
 * Properties like `count` become `this.count`
 * This allows access to data, computed, and methods
 */
function transformExpression(expression, skipVars) {
	const keywords = new Set([
		'true', 'false', 'null', 'undefined', 'this',
		'if', 'else', 'for', 'while', 'do', 'switch', 'case', 'break', 'continue', 'return',
		'function', 'var', 'let', 'const', 'class', 'new', 'delete', 'typeof', 'instanceof',
		'in', 'of', 'try', 'catch', 'finally', 'throw', 'async', 'await', 'yield',
		'import', 'export', 'default', 'from', 'void'
	]);

	const globals = new Set([
		'Math', 'Date', 'JSON', 'Array', 'Object', 'String', 'Number', 'Boolean',
		'parseInt', 'parseFloat', 'isNaN', 'isFinite', 'console', 'window', 'document',
		'RegExp', 'Error', 'Promise', 'Set', 'Map', 'WeakSet', 'WeakMap', 'Symbol',
		'Infinity', 'NaN', 'encodeURI', 'decodeURI', 'encodeURIComponent', 'decodeURIComponent',
		'$event' // Event object in inline handlers
	]);

	let result = '';
	let i = 0;

	while (i < expression.length) {
		const char = expression[i];

		// Handle string literals
		if (char === '"' || char === "'" || char === '`') {
			const quote = char;
			result += char;
			i++;

			while (i < expression.length) {
				const c = expression[i];
				result += c;
				if (c === quote && expression[i - 1] !== '\\') {
					i++;
					break;
				}
				i++;
			}
			continue;
		}

		// Handle identifiers
		if (isIdentifierStart(char)) {
			let identifier = '';

			while (i < expression.length && isIdentifierChar(expression[i])) {
				identifier += expression[i];
				i++;
			}

			// Check if this is after a dot (property access)
			const beforeIdent = result.trimEnd();
			const isAfterDot = beforeIdent.endsWith('.');

			if (isAfterDot) {
				// Already accessing a property, don't prefix
				result += identifier;
			} else if (keywords.has(identifier)) {
				// JS keyword, don't prefix
				result += identifier;
			} else if (globals.has(identifier)) {
				// Global object, don't prefix
				result += identifier;
			} else if (skipVars && skipVars.has(identifier)) {
				// Loop variable passed as function parameter, don't prefix
				result += identifier;
			} else {
				// Component property - prefix with this.
				result += 'this.' + identifier;
			}
			continue;
		}

		// Handle numbers
		if (isDigit(char)) {
			while (i < expression.length && (isDigit(expression[i]) || expression[i] === '.')) {
				result += expression[i];
				i++;
			}
			continue;
		}

		result += char;
		i++;
	}

	return result;
}

function isIdentifierStart(char) {
	return /[a-zA-Z_$]/.test(char);
}

function isIdentifierChar(char) {
	return /[a-zA-Z0-9_$]/.test(char);
}

function isDigit(char) {
	return /[0-9]/.test(char);
}

/**
 * Generate AST back to HTML string
 * Strips reactive bindings and directives for static template
 * Minifies whitespace
 */
export function astToHTML(ast) {
	if (!ast) return '';

	switch (ast.type) {
		case 'root':
			return ast.children.map(astToHTML).join('');

		case 'element':
			return elementToHTML(ast);

		case 'text':
			// Collapse whitespace: replace multiple spaces/newlines with single space
			return collapseWhitespace(ast.content);

		case 'binding':
			// Wrap in span to prevent text node merging with adjacent text
			return '<span>\u200B</span>';

		case 'comment':
			return `<!--${ast.content}-->`;

		default:
			return '';
	}
}

/**
 * Collapse whitespace in text content
 * - Replace multiple whitespace chars with single space
 * - Trim leading/trailing whitespace
 */
function collapseWhitespace(text) {
	return text.replace(/\s+/g, ' ');
}

/**
 * Reactive attribute prefixes to strip
 */
const REACTIVE_ATTR_PREFIXES = [':', '@'];

/**
 * Dynamic directive attributes to strip
 */
const DYNAMIC_DIRECTIVES = [':for', ':if', ':else-if', ':else'];

/**
 * Convert element node to HTML
 */
function elementToHTML(node) {
	const tag = node.tag;

	// Filter out reactive attributes
	const staticAttrs = Object.entries(node.attributes || {})
		.filter(([name]) => {
			if (DYNAMIC_DIRECTIVES.includes(name)) return false;
			if (REACTIVE_ATTR_PREFIXES.some(p => name.startsWith(p))) return false;
			return true;
		})
		.map(([name, value]) => {
			if (value === '' || value === true) return name;
			return `${name}="${escapeAttr(value)}"`;
		})
		.join(' ');

	const attrStr = staticAttrs ? ' ' + staticAttrs : '';

	const voidElements = new Set([
		'area', 'base', 'br', 'col', 'embed', 'hr', 'img', 'input',
		'link', 'meta', 'param', 'source', 'track', 'wbr'
	]);

	if (node.selfClosing || voidElements.has(tag)) {
		return `<${tag}${attrStr} />`;
	}

	const children = node.children ? node.children.map(astToHTML).join('') : '';
	return `<${tag}${attrStr}>${children}</${tag}>`;
}

function escapeAttr(value) {
	return String(value)
		.replace(/&/g, '&amp;')
		.replace(/"/g, '&quot;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;');
}

/**
 * Generate compact binary output (for production)
 */
export function generateBinary(compilation) {
	const json = JSON.stringify({
		template: compilation.templateHTML,
		binding: {
			ref: Array.from(new Set(compilation.bindings.flatMap(b => b.properties || []))),
			code: Array.from(compilation.bytecode)
		},
		dynamics: compilation.dynamics
	});

	const encoder = new TextEncoder();
	return encoder.encode(json).buffer;
}

export default {
	generateCode,
	astToHTML,
	generateBinary
};
